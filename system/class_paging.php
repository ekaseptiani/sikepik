// class paging untuk halaman administrator
class Paging{
// Fungsi untuk mencek halaman dan posisi data
function cariPosisi($batas){
if(empty($_GET['halaman'])){
	$posisi=0;
	$_GET['halaman']=1;
}
else{
	$posisi = ($_GET['halaman']-1) * $batas;
}
return $posisi;
}

// Fungsi untuk menghitung total halaman
function jumlahHalaman($jmldata, $batas){
$jmlhalaman = ceil($jmldata/$batas);
return $jmlhalaman;
}

// Fungsi untuk link halaman 1,2,3 (untuk admin)
function navHalaman($halaman_aktif, $jmlhalaman){
$link_halaman = "";

// Link ke halaman pertama (first) dan sebelumnya (prev)
if($halaman_aktif > 1){
	$prev = $halaman_aktif-1;
	$link_halaman .= "<li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=1> << </a></span></li>
						<li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$prev> < </a></span></li> ";
}
else{ 
	$link_halaman .= " <li><span> << </span></li><li><span> < </span></li> ";
}

// Link halaman 1,2,3, ...
$angka = ($halaman_aktif > 3 ? " <li><span>...</span></li> " : " "); 
for ($i=$halaman_aktif-2; $i<$halaman_aktif; $i++){
  if ($i < 1)
  	continue;
	  $angka .= "<li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$i>$i</a></span></li> ";
  }
	  $angka .= " <li class='active'><span>$halaman_aktif</span></li> ";
	  
    for($i=$halaman_aktif+1; $i<($halaman_aktif+3); $i++){
    if($i > $jmlhalaman)
      break;
	  $angka .= "<li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$i>$i</a></span></li> ";
    }
	  $angka .= ($halaman_aktif+2<$jmlhalaman ? " <li><span>...</span></li> <li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$jmlhalaman>$jmlhalaman</a></span></li> " : " ");

$link_halaman .= "$angka";

// Link ke halaman berikutnya (Next) dan terakhir (Last) 
if($halaman_aktif < $jmlhalaman){
	$next = $halaman_aktif+1;
	$link_halaman .= " <li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$next> > </a></span></li>
						<li><span><a href=$_SERVER[PHP_SELF]?module=$_GET[module]&halaman=$jmlhalaman> >> </a></span></li> ";
}
else{
	$link_halaman .= " <li><span> > </span></li><li><span> >> </span></li>  ";
